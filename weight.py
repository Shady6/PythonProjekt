# imperial to metric

def gr_to_mg(x):
    return x * 64.79891


def dr_to_gr(x):
    return x * 27.34375 * 64.79891


def oz_to_gr(x):
    return x * 16 * 27.34375 * 64.79891


def lb_to_gr(x):
    return x * 16 * 16 * 27.34375 * 64.79891


def st_to_gr(x):
    return x * 14 * 16 * 16 * 27.34375 * 64.79891


def qt_to_gr(x):
    return x * 2 * 14 * 16 * 16 * 27.34375 * 64.79891


def cwt_to_gr(x):
    return x * 4 * 2 * 14 * 16 * 16 * 27.34375 * 64.79891


def t_to_gr(x):
    return x * 20 * 4 * 2 * 14 * 16 * 16 * 27.34375 * 64.79891


def mg_to_g(x):
    return x * 0.001


def mg_to_dkg(x):
    return x * 0.0001


def mg_to_kg(x):
    return x * 0.000001


def mg_to_t(x):
    return x * 0.000000001

